        public static Route? GetDirectConnection(int fromCityId, int toCityId, TransportMode mode)
        {
            var allRoutes = new List<Route>();
            allRoutes.AddRange(GetAllRoadConnections());
            allRoutes.AddRange(GetAllRailConnections());
            allRoutes.AddRange(GetAllAirConnections());

            return allRoutes.FirstOrDefault(r =>
                (r.FromCityId == fromCityId && r.ToCityId == toCityId && r.TransportMode == mode) ||
                (r.FromCityId == toCityId && r.ToCityId == fromCityId && r.TransportMode == mode)
            );
        }

        public static List<City> GetCitiesByProvince(string province)
        {
            return GetAllCities().Where(c => c.Province == province).ToList();
        }

        public static City? GetCityById(int id)
        {
            return GetAllCities().FirstOrDefault(c => c.Id == id);
        }

        public static List<City> GetMajorCities()
        {
            return GetAllCities().Where(c => c.IsMetroCity).ToList();
